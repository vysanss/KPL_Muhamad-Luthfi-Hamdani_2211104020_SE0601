def check_hari (hari):
    if hari == 1:
        return "Senin"
    elif hari == 2:
        return "Selasa"
    elif hari == 3:
        return "Rabu"
    elif hari == 4:
        return "Kamis"
    elif hari == 5:
        return "Jumat"
    elif hari == 6:
        return "Sabtu"
    elif hari == 7:
        return "Minggu"
    else:
        return "Hari tidak valid"